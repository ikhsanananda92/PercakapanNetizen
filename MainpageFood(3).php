                            <p>Kedai kopi lokasinya bisa berada di mana saja, dari gerobak motor yang dibuat lapak, halaman rumah, dan bahkan rumah adat.</p>
                            <p>Inilah yang unik dari kedai kopi Cofee Kawan Kita. Kedai kopi di Pematang Siantar, Sumatera Utara ini bertempat di rumah adat Batak Toba.</p>
                            <p>"Rumah adat ini sebelumnya ada di Tambunan, Kabupaten Toba. Kita bongkar dan angkut dari sana terus kita bangun di sini," kata pemilik Cofee Kawan Kita Lamhot Siahaan pada Tribunnews Medan, Kamis (29/4/2021).</p>
                            <p>Lamhot mengatakan usia rumah adat tersebut sudah 200 tahunan.</p>
                            <p>Ia menjelaskan rumah adat Batak Toba itu awalnya dibeli untuk jadi tempat tinggal ketika orang tuanya membeli tanah di Pematang Siantar.</p>
                            <p>Seiring waktu, muncul ide memanfaatkan rumah adat tersebut menjadi kedai kopi.</p>
                            <p>Rumah adat itu sendiri masih tampak kokoh. Lokasinya juga asri karena tepat menghadap ke pemandangan sawah, barisan bukit, dan rumah warga.</p>
                            <p>"Memang suka ngopi dan mungkin kan karena ada rezeki sedikit. Rumah adat sudah ada di sini dari 2012, kita coba buat lokasi ngopi daripada ngontrak," ujar Lamhot.</p>
                            <p>Lamhot mengaku usaha kedai kopinya ini baru dimulai dari awal 2021.</p>
                            <p>Ia berharap kedai kopi di rumah adat ini dapat menjadi daya tarik bagi penikmat kopi dan wisatawan.</p>
                            <p>Jenis kopi yang ditawarkan di Cofee Kawan Kita adalah Arabica yang berasal dari Tanah Gayo, Sidikalang, Simalungun, dan Doloksanggul.</p>
                            <p>Kopi dibuat jadi berbagai jenis minuman, tetapi favorit pengunjung kedai adalah sanger espresso.</p>
                            <p>Cofee Kawan Kita beralamat di Jalan Bahkora II Atas, Kelurahan Siantar Tong Marimbun, Kecamatan Siantar Marimbun, Kota Pematangsiantar. Kedai kopi ini buka setiap hari dari pukul 12.00-00.00 WIB.</p>
