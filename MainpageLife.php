                        <div class="post-title">
                            <a href="MainpageLife.php">Midsole 3D Printing pada Sepatu Lari Terbaru Adidas, Apa Istimewanya?</a>
                            <p>Merek perlengkapan olahraga Adidas merancang sepatu lari baru dengan membekali fitur yang canggih di dalamnya
                            </p>
                            <p>Diberi nama 4DFWD, sepatu lari ini akan memudahkan pemakainya melangkah setiap kali kaki menginjak tanah.
                            </p>
                            <p>Dalam mendesain sepatu 4DFWD, Adidas bekerja sama dengan perusahaan 3D printing Carbon.
                            </p>
                            <p>Perusahaan Carbon membuat midsole dengan cetakan 3D yang memiliki lubang-lubang kecil berbentuk dasi kupu-kupu.
                            </p>
                            <p>Saat mendapat tekanan, sepatu ini akan membuat kaki pemakainya maju ketimbang posisi midsole ke tanah.
                            </p>
                            <p>Sementara, cara kerja midsole konvensional hanya menekan ke bawah, sehingga kaki kita lebih kuat menekuk bagian depan sepatu.
                            </p>
                            <p>Adidas dan Carbon mengatakan, midsole yang didesain ulang ini memangkas gaya pengereman dan mendorong bagian depan kaki 15 persen dibandingkan sepatu biasa.
                            </p>
                            <p>Sepatu 4DFWD sudah dirilis oleh Adidas sejak hari Rabu (19/5/2021) kemarin.
                            </p>
                            <p>
                                <div>
                                    <img src="Main Page/assets/sepatu1.jpg" class="img" alt="blog1">
                                </div>
                            </p>
                            <p>"Kami mengidentifikasi satu midsole kisi sempurna yang dirancang untuk menekan kaki ke depan."
                            </p>
                            <p>"Lalu midsole ini pun melawan kekuatan mekanis sambil memberikan sensasi melaju yang unik bagi pemakainya."
                            </p>
                            <p>Demikian dipaparkan Sam Handy, Vice President of Design for Running shoes di Adidas.
                            </p>
                            <p>Desain baru ini menggambarkan perubahan radikal di bidang manufaktur yang dimungkinkan oleh perusahaan 3D printing.
                            </p>
                            <p>Selain itu, desain yang ada pada sepatu 4DFWD tidak dapat dibuat lewat metode pengecoran, molding, ekstrusi, atau mesin konvensional.
                            </p>
                            <p>Sebuah survei terhadap 1.900 perusahaan 3D menemukan 52 persen perusahaan menggunakan 3D printing untuk membuat produk, tidak sekadar prototipe.
                            </p>
                            <p>Survei itu dilakukan Sculpteo, perusahaan 3D printing yang merupakan anak perusahaan asal Jerman, BASF.
                            </p>
                            <p>Metode 3D printing paling sering digunakan untuk membuat bentuk kompleks dan kustomisasi massal yang disesuaikan secara digital.
                            </p>
                            <p>Proses pembuatan produk yang dilakukan oleh perusahaan Carbon, yaitu Digital Light Synthesis, berbeda dari kebanyakan 3D printing.
                            </p>
                            <p>Digital Light Synthesis memancarkan sinar ultraviolet secara hati-hati ke dalam kumpulan tipis resin cair yang mengeras dalam cahaya.
                            </p>
                            <p>Saat produk terbentuk, secara bertahap bahan akan terangkat dan resin baru mengeras. Hasilnya diklaim menjadi bahan yang lebih konsisten dan kuat.
                            </p>
                            <p>3D printing mulai mendapat perhatian selama pandemi, ketika perusahaan dan rumah tangga menganggap teknologi tersebut berguna untuk memproduksi peralatan pelindung pribadi seperti pelindung wajah (face shield).
                            </p>
                            <p>
                                <div>
                                    <img src="Main Page/assets/sepatu2.jpg" class="img" alt="blog1">
                                </div>
                            </p>
                            <p>Adidas dan Carbon mengevaluasi 5 juta kemungkinan struktur kisi sebelum menetapkan pola dasi kupu-kupu untuk sepatu 4DFWD.
                            </p>
                            <p>Kedua kolaborator menguji desain sepatu tersebut dengan atlet lari, serta para ahli di University of Calgary dan University of Arizona.
                            </p>
                            <p>Adidas 4DFWD diprediksi akan hadir di pasaran mulai 1 Juli dengan harga sekitar 240 dollar AS atau setara Rp 3,4 juta.
                            </p>
