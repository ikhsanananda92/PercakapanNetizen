                        <a href="MainpageFood(8).php">3 Tips Pisahkan Daging Iga Kambing dari Tulangnya</a>
                            <p>Salah satu bagian kambing yang mudah untuk diolah adalah bagian iganya. Iga kambing bisa dimasak dalam waktu yang cepat karena mudah empuk.</p>
                            <p>Salah satu resep iga kambing sempat dipraktikkan oleh Executive Chef Hotel Santika Mega City Sadar Bonar Nababan.</p>
                            <p>Bonar memasak sajian Iga Kambing Sambal Jambrong dalam sesi Live IG Kangen Masakan Kampungku di Instagram @my.foodplace, Kamis (3/6/2021). </p>
                            <p>Iga kambing yang dimasak Bonar sebelumnya telah dipisahkan dari bagian tulangnya sehingga lebih cepat matang dan mudah dimakan.</p>
                            <p>Bonar memberikan beberapa tips mudah memisahkan daging iga kambing dari tulangnya seperti berikut:</p>
                            <a>1. Gunakan pisau tajam</a>
                            <p>Pertama adalah mempersiapkan pisau yang punya ketajaman baik. Dengan pisau yang tajam, maka daging akan lebih mudah terpotong. Nantinya hasil potongan akan lebih halus dan tidak berantakan.</p>
                            <p>
                                <div>
                                    <img src="Main Page/assets/iga1.jpg" class="img" alt="blog1">
                                </div>
                            </p>
                            <a>2. Ikuti arah tulang</a>
                            <p>Kemudian gunakan pisau untuk memotong daging iga mengikuti arah tulang. Jangan lupa untuk membuang bagian lemaknya sebanyak mungkin karena bagian lemak ini salah satu yang menyebabkan daging kambing jadi bau.</p>
                            <p>“Yang pasti jalur potongnya ya, jalur potongnya harus tipis gitu,” ujar Bonar.</p>
                            <p>Pisahkan daging dan tulang setipis mungkin satu sama lain agar tidak banyak daging bagus yang tersisa menempel pada tulang.</p>
                            <a>3. Potong dalam keadaan dingin</a>
                            <p>Terakhir, adalah memotong daging iga dalam keadaan dingin. Menurut Bonar, suhu dingin daging akan sangat membantu mempertahankan tekstur daging saat dipisahkan dari tulang. Tekstur akan tetap rapi.</p>
                            <p>“Kita potong saat temperaturnya di sekitar 5 – 10 derajat celsius. Temperatur itu membantu tekstur pada saat dipotong lebih mudah dan rapi,” jelas Bonar.</p>
                            <p>Jika kamu membelinya tidak dalam keadaan dingin atau beku, sebaiknya masukkan dulu daging kambing ke dalam freezer selama beberapa jam hingga cukup dingin untuk dipotong.</p>
