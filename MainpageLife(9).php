                            <p>Apakah kamu lebih mudah marah saat merasa lapar? Ya, rasa lapar bisa membuat kemarahan seseorang meledak, dan ini bukan hal yang asing bagi kita.</p>
                            <p>Ketika merasa sangat lapar, tanpa disadari kita menjadi mudah tersinggung bahkan bereaksi berlebihan terhadap sesuatu, sekalipun itu adalah masalah kecil.</p>
                            <p>Tapi apakah ini benar ada kaitannya dengan fenomena fisiologis, atau hanyalah sebuah alasan semata?</p>
                            <p>Christine Lee, MD, seorang ahli gastroenterologi di Amerika Serikat mengatakan bahwa ada alasan fisiologis mengapa beberapa orang jadi mudah marah ketika lapar.</p>
                            <p>Hal ini dikarenakan oleh kadar gula (glukosa) dalam darah menurun, jika kita tidak makan untuk beberapa waktu.</p>
                            <p>Kondisi gula darah yang terlalu rendah dapat memicu aliran hormon, termasuk kortisol (hormon stres) dan adrenalin. Hormon-hormon ini dilepaskan ke aliran darah untuk meningkatkan dan menyeimbangkan kembali gula darah kita.</p>
                            <p>Lalu, pelepasan kortisol inilah yang menyebabkan emosi meningkat pada beberapa orang.</p>
                            <p>"Juga, gula darah yang rendah dapat mengganggu fungsi otak yang lebih tinggi, sehingga kita sulit mengendalikan impuls dan dorongan perilaku primitif kita," kata dr. Lee.</p>
                            <p>Jadi, memang ada penjelasan medis mengapa kita begitu mudah marah ketika merasa lapar.</p>
                            <p>Ini adalah reaksi biokimia karena gula darah rendah, jadi ini tidak sama dengan rasa marah ketika kita merasa lelah, sakit, atau merasa tidak enak badan.</p>
                            <p>Perlu kita ketahui, rasa lapar yang dibiarkan, tak hanya membuat kita jadi cepat emosi saja. Namun, ada beberapa konsekuensi negatif lainnya yang bisa kita alami, yaitu:</p>
                            <p>
                                <ul>
                                    <li>Kelelahan.</li>
                                    <li>Kantuk</li>
                                    <li>Kesulitan berkonsentrasi.</li>
                                    <li>Koordinasi yang buruk.</li>
                                    <li>Kerentanan untuk membuat kesalahan.</li>
                                </ul>
                            </p>
                            <p>Nah, untuk mengendalikan emosi akibat rasa lapar, kita bisa melakukan langkah-langkah  berikut ini:</p>
                            <p>
                                <ol>
                                    <li>Makan beberapa kali dalam porsi kecil sepanjang hari, atau memastikan diri untuk sarapan, makan siang dan makan malam yang mengenyangkan dan bergizi.</li>
                                    <li>Menghindari junk food, yang dapat menyebabkan gula darah rendah. Makanan kaya nutrisi dan tinggi serat adalah pilihan terbaik dan membuat kita merasa kenyang lebih lama.</li>
                                    <li>Menyediakan camilan sehat dan praktis di dalam tas, mobil, atau meja kerja yang dapat memberikan ketenangan pikiran.</li>
