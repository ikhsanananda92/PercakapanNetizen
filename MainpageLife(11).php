                        <a href="MainpageLife(11).php">Cara Mudah Kurangi Asupan Kalori, Cobalah Makan Pelan-pelan</a>
                            <p>Terkadang kita makan secara berlebihan bukan cuma karena kelezatan makanannya, tapi juga besar kecilnya porsi dan kecepatan kita saat makan.</p>
                            <p>Ketika kita diberikan porsi makanan yang lebih besar, maka kita pun akan mengasup lebih banyak, yang berarti jumlah kalorinya juga tinggi.</p>
                            <p>Demikian kesimpulan studi terbaru, yang juga menemukan bahwa orang yang mengunyah makanannya dengan cepat cenderung makan lebih banyak.</p>
                            <p>Peneliti yang melakukan studi tentang hal ini, Paige Cunningham, mahasiswa doktoral di Pennsylvania State University, mengatakan temuannya ini memiliki implikasi pada kesehatan, terutama pencegahan kegemukan.</p>
                            <p><b>Metode studi</b></p>
                            <p>Penelitian tersebut melibatkan 44 pria dan wanita berusia antara 18 hingga 68 tahun, dengan dua pertiganya adalah perempuan. Sekitar 45 persen peserta mengalami kelebihan berat badan.</p>
                            <p>Para peneliti kemudian memberi mereka makaroni dan keju dalam porsi yang berbeda secara acak untuk menu makan siang selama empat minggu.</p>
                            <p>Peneliti juga merekam makanan tersebut sehingga mereka bisa menilai kecepatan para peserta saat makan dan ukuran gigitannya.</p>
                            <p>Hasil dari penelitian tersebut adalah para peserta akan makan lebih banyak ketika mereka makan lebih cepat dibandingkan dengan peserta yang makan secara lambat.</p>
                            <p><b>Makan cepat membuat orang lebih banyak makan</b></p>
                            <p>Alasan orang makan lebih banyak saat mereka makan lebih cepat bisa jadi karena respons tubuh terhadap berapa lama makanan di mulut seseorang.</p>
                            <p>"Ketika kita makan dengan sangat cepat, jelas makanan menghabiskan lebih sedikit waktu di mulut dan ketika kita menggigit sangat besar, makanan hanya sebentar di mulut kita," terang Cunningham.</p>
                            <p>"Jadi, dibutuhkan lebih lama bagi sinyal-sinyal ini memberi tahu kita untuk berhenti makan dan kita akhirnya makan lebih banyak ketika kita makan dengan lebih cepat," sambung dia.</p>
                            <p>Karena itu, kita perlu menyadari porsi yang kita ambil.</p>
                            <p>Profesor departemen ilmu nutrisi di College of Health and Human Development di Penn State, Barbara Rolls mengatakan, meskipun memperlambat makan mungkin menjadi pilihan untuk mengurangi konsumsi berlebihan, namun itu sulit dilakukan. Dan, beberapa bukti menunjukkan kalau kecepatan makan adalah perilaku berbasis genetik.</p>
                            <p>Selain memperlambat makan, Rolls juga menyarankan agar mengurangi makanan padat kalori dan menambahkan lebih banyak sayuran, menggunakan lebih banyak bumbu maupun rempah-rempah untuk mempertahankan makanan tetap lezat.</p>
                            <p>Sementara itu, asisten profesor di Fielding School of Public Health di UCLA, Dana Hunnes mengungkapkan bahwa ada beberapa data selama bertahun-tahun yang menunjukkan bertambahnya ukuran porsi dapat membuat orang cenderung makan lebih banyak.</p>
                            <p>"Tubuh kita memerlukan waktu sekitar 15 hingga 20 menit untuk menyadari bahwa kita sudah kenyang dan mulai menjalani proses pencernaan," tuturnya.</p>
                            <p>Hunnes, yang juga mengadvokasi tentang diet nabati, mengatakan bahwa dia tidak suka melihat orang kelaparan ketika mereka mencoba untuk memangkas porsi makan untuk diet.</p>
                            <p>Namun, dia merekomendasikan agar kita tetap makan dengan menambahkan banyak sayuran rendah kalori, buah, dan biji-bijian ke dalam makanan karena dapat membuat perut tak mudah lapar.</p>
