                        <a href="MainpageFood(10).php">Tips Masak Poached Egg agar Tidak Hancur, Pakai Cuka</a>
                            <p>Poached egg merupakan telur yang direbus tanpa cangkang. Sering juga disebut telur ceplok rebus atau telur apung.</p>
                            <p>Untuk mendapatkan poached egg yang cantik dan menggugah selera memang cukup rumit. Pasalnya, seringkali hasil poached egg malah berantakan. Kuning telurnya pun tak utuh.</p>
                            <p>Executive Chef Hotel Santika Mega City Bekasi Sadar Bonar Nababan mengatakan bahwa untuk bisa menghasilkan poached egg yang sempurna diperlukan tambahan bahan tertentu dan juga trik khusus, yakni menggunakan cuka.</p>
                            <p>“Jadi dia itu akan memberikan cairan asam, di mana air asam itu akan mengikat telur yang direbus,” kata Bonar ketika memasak sajian Laksa Nusantara dalam Live IG Kangen Masakan Kampungku di Instagram @my.foodplace, Kamis (3/6/2021).</p>
                            <p>Cuka dicampurkan ke dalam air untuk merebus telur. Cuka ini akan membuat bagian putih telur lebih mudah terikat sehingga tidak berantakan. Putih telur akan mulus dan menahan bagian kuning telur agar tidak hancur.</p>
                            <p>“Cuka ini bisa juga diganti jeruk nipis, tapi kurang strong nantinya kurang mengikat,” imbuh Bonar.</p>
                            <p>
                                <div>
                                    <img src="Main Page/assets/gomak1.jpg" class="img" alt="blog1">
                                </div>
                            </p>
                            <a>Cara membuat poached egg dengan cuka</a>
                            <p>Cara membuat poached egg dengan campuran cuka cukup mudah. Cukup campurkan cuka secukupnya ke dalam panci berisi air. Aduk sebentar, lalu didihkan air.</p>
                            <p>Kemudian pecahkan telur ke dalam mangkuk terpisah. Jika air dalam panci sudah mendidih, masukkan telur yang sudah dipecahkan tadi secara perlahan agar tidak hancur.</p>
                            <p>Masak telur dalam panci sekitar 30 detik untuk mendapatkan hasil setengah matang. Namun jika air yang digunakan untuk memasak telur kurang mendidih, tunggu hingga sekitar satu menit.</p>
                            <p>“Telur direbus sekitar setengah menit sudah jadi, kalau menggunakan air mendidih (100-110 derajat celsius. Kalau ini air suhu rendah, sekitar 90 derajat celsius ke bawah, itu sekitar satu menit poached egg sudah jadi,” jelas Bonar.</p>
                            <p>Setelah itu angkat poached egg dengan saringan. Letakkan dalam wadah terpisah, lalu bilas dengan air tawar beberapa kali untuk menghilangkan rasa asam dari cuka pada telur.</p>
                            <p>Poached egg yang mulus pun siap disajikan. Poached egg ini cocok jadi topping beragam sajian. Seperti Laksa Nusantara, mi rebus, atau sajian sarapan.</p>
