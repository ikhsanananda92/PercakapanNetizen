                            </p>
                            <p>Misalnya, dengan melakukan zoom-in, pengguna bisa melihat nama-nama bintang dengan jelas. Lalu nama bintang itu juga bisa diklik dan akan menampilkan visual dan informasi singkat dari bintang tersebut.
