                            <p>Setelah itu, pengguna hanya perlu mengklik ikon play atau opsi "take a tour" di pojok kiri atas halaman, untuk mulai menjelajahi tata surya dan melihat 100.000 bintang yang telah berhasil didokumentasikan oleh Google.
                            </p>
                            <p>Pengguna akan dibawa mendekati pusat tata surya, yaitu matahari. Kemudian, pengguna akan perlahan diperlihatkan empat planet yang paling dekat dengan matahari.
                            </p>
                            <p>Lalu Google akan membawa kita semakin jauh dari matahari dan menampakkan beberapa bintang yang paling dekat dengan tata surya, di antaranya ada bintang bernama Altair, Kruger 60, Bernard's Star, Sirius, Proxima Centauri, dan
